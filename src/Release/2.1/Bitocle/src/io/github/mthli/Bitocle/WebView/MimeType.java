    public static boolean isMarkdown(String fileName) {
        for (int i = 0; i < MARKDOWN_EXTENSIONS.length; i++) {
            if (fileName.toLowerCase().endsWith(MARKDOWN_EXTENSIONS[i])) {
                return true;
            }
        }

        return false;
    }

    public static boolean isImage(String fileName) {
        for (int i = 0; i < IMAGE_EXTENSIONS.length; i++) {
            if (fileName.toLowerCase().endsWith(IMAGE_EXTENSIONS[i])) {
                return true;
            }
        }

        return false;
    }

    public static boolean isUnSupport(String fileName) {
        for (int i = 0; i < UNSUPPORT_EXTENSIONS.length; i++) {
            if (fileName.toLowerCase().endsWith(UNSUPPORT_EXTENSIONS[i])) {
                return true;
            }
        }

        return false;
    }

    public static String getImageExtension(String fileName) {
        String[] arr = {
                "bmp",
                "gif",
                "ico",
                "jpeg",
                "jpg",
                "png"
        };

        int i;
        for (i = 0; i < arr.length; i++) {
            if (fileName.toLowerCase().endsWith(arr[i])) ;
            break;
        }

        return arr[i];
    }
